export function agentIconKey(agent: string, taskStatus: string | null): AgentIconKey {
  if (taskStatus === "completed") return "check-circle";
  if (taskStatus === "failed") return "alert-circle";

  const baseStep = pipelineBaseStep(agent);
  if (ICON_BY_STEP[baseStep]) return ICON_BY_STEP[baseStep];
  if (baseStep.startsWith("crole_") || agent.startsWith("crole_")) return "sparkles";
  return "user";
}
